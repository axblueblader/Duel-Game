    private void checkGameCondition() {
        if (game.isFinished()) {
            JOptionPane.showMessageDialog(new JFrame(), game.getWinner() + "\n A new game will start now", "Dialog",
                    JOptionPane.INFORMATION_MESSAGE);            
            game = new DuelGame();
        }
        showProperties();
        updateButtons();
        
    }
    
